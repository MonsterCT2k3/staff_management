class Employee {
  final int id;
  final String name;
  final String gender;
  final String email;
  final String phone;
  final String role;
  final String address;
  final bool isAdmin;
  final String department;

  Employee(
      this.id, this.name, this.gender, this.email, this.phone, this.role, this.address, this.isAdmin, this.department);
}
